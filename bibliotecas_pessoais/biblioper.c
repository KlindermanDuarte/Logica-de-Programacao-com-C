#include "biblioper.h"

float funcaoSoma(float numero_para_soma_um, float numero_para_soma_dois){
    return numero_para_soma_um + numero_para_soma_dois;
}
float funcaoMultiplicacao(float numero_para_multiplicacao_um, float numero_para_multiplicacao_dois){
    return numero_para_multiplicacao_um * numero_para_multiplicacao_dois;
}
float funcaoSubtracao(float numero_para_subtracao_um, float numero_para_subtracao_dois){
    return numero_para_subtracao_um - numero_para_subtracao_dois;
}
float funcaoDivisao(float numero_para_divisao_um, float numero_para_divisao_dois){
    return numero_para_divisao_um / numero_para_divisao_dois;
}